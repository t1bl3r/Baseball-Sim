
public class Player {
	public String name;
	public double singleAvg;
	public double doubleAvg;
	public double tripleAvg;
	public double homerunAvg;
	public double walkAvg; 
	
	
	public Player(String name, double singleAvg, double doubleAvg, double tripleAvg, double homerunAvg, double walkAvg) {
		this.name = name;
		this.singleAvg = singleAvg;
		this.doubleAvg = doubleAvg;
		this.tripleAvg = tripleAvg;
		this.homerunAvg = homerunAvg;
		this.walkAvg = walkAvg;
	}
	
}
